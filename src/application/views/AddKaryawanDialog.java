    private void tombolEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tombolEditMouseClicked
        // TODO add your handling code here:
        try {
            if (!isEdit) {
            } else if (!textFieldNama.getText().equals("")
                    && !textFieldPosisi.getText().equals("")) {
                KaryawanModel k = new KaryawanModel();
                k.setId(Integer.parseInt(textFieldId.getText()));
                k.setNama(textFieldNama.getText());
                k.setPosisi(textFieldPosisi.getText());
                k.setJenis_kelamin(jComboBoxjeniskelamin.getSelectedItem().toString());
                karyawanDao.update(k);

                this.clearForm();

                this.dispose();
            } else {
                IsiKosong.showMessageDialog(rootPane, "Mohon isi semua kolom isian pada form !", "Error", ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Mengubah Data");
        }
    }//GEN-LAST:event_tombolEditMouseClicked

