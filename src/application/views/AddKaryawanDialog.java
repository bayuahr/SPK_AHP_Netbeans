    private void buttonAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAddMouseClicked
        // TODO add your handling code here:
        try {
            if (isEdit) {
            } else if (!textFieldPosisi.getText().equals("")
                    && !textFieldNama.getText().equals("")) {
                KaryawanModel k = new KaryawanModel();
                k.setNama(textFieldNama.getText());
                k.setPosisi(textFieldPosisi.getText());
                k.setJenis_kelamin(jComboBoxjeniskelamin.getSelectedItem().toString());

                karyawanDao.insertOne(k);

                this.clearForm();

                this.dispose();
            } else {
                IsiKosong.showMessageDialog(rootPane, "Mohon isi semua kolom isian pada form !", "Error", ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Menambah Data");
        }
    }//GEN-LAST:event_buttonAddMouseClicked

