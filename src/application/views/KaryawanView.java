    private void getDataTabel() {
        int selectedRow = tableCandidate.getSelectedRow();

        TableModel model = tableCandidate.getModel();
        int code = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        String name = model.getValueAt(selectedRow, 1).toString();
        String description = model.getValueAt(selectedRow, 2).toString();
        String posisi = model.getValueAt(selectedRow, 3).toString();
        this.code = code;

        addKaryawanDialog.setDataTabel(code,name, description, posisi);
    }

    public void loadTable() {
        List<KaryawanModel> products = this.karyawanDao.findAll();
        KaryawanTableModel productTableModel = new KaryawanTableModel(products);

        tableCandidate.setModel(productTableModel);

        TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tableCandidate.getModel());
        tableCandidate.setRowSorter(sorter);

        List<RowSorter.SortKey> sortKeys = new ArrayList<>(25);
        sortKeys.add(new RowSorter.SortKey(0, SortOrder.ASCENDING));
        sortKeys.add(new RowSorter.SortKey(1, SortOrder.ASCENDING));
        sortKeys.add(new RowSorter.SortKey(2, SortOrder.ASCENDING));
        sortKeys.add(new RowSorter.SortKey(3, SortOrder.ASCENDING));
        sorter.setSortKeys(sortKeys);
    }

