    private void buttonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditActionPerformed
        // TODO add your handling code here:
        if (code != 0) {
            addKaryawanDialog.setTitleFrame("Ubah Data Karyawan");
            addKaryawanDialog.show();
            loadTable();
            code = 0;
        } else {
            JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah! ");
        }
    }//GEN-LAST:event_buttonEditActionPerformed

    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        // TODO add your handling code here:
        try {
            if (code != 0) {
                int ok = JOptionPane.showConfirmDialog(null, "Hapus data karyawan?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
                if (ok == 0) {
                    int deleted = karyawanDao.delete(code);
                    if (deleted > 0) {
                        JOptionPane.showMessageDialog(null, "Data Berhasil dihapus ");
                        loadTable();
                        System.out.println(code);

                        code = 0;
                        addKaryawanDialog.clearForm();
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal Menghapus Data");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus! ");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Menghapus Data");
        }
    }//GEN-LAST:event_buttonDeleteActionPerformed

